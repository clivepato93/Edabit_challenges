def win_round(you, opp):
    you,opp=sorted(you),sorted(opp)
    myhand=int("".join(str(l) for l in you[-2:][::-1]))
    opphand=int("".join(str(l) for l in opp[-2:][::-1]))
    # return False if you==opp else True
    return False if myhand==opphand else True if myhand>opphand else False

